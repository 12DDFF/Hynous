"""Experiment 4: Exit Model (Model B)

Predicts: At each 5-minute checkpoint while holding a position,
          should you HOLD or EXIT?
Features: 14 market features + 5 position context features = 19 total.
Label: should_hold (binary) from simulated_exits table.

This is the HIGHEST IMPACT experiment. The simulated_exits data already
exists in satellite.db — generated by the labeler.

Position context features derived from simulated_exits columns:
  - pos_current_roe: ROE at this checkpoint
  - pos_roe_drawdown: how far ROE has fallen from peak (0 = at peak, negative = fading)
  - pos_time_in_position: minutes held (5, 10, 15, 20, 25, 30)
  - pos_roe_velocity: ROE change from previous checkpoint
  - pos_side_encoded: 1 for long, -1 for short

Statistical notes:
  - Walk-forward splits on snapshot time (not exit row index) to prevent
    temporal contamination. Multiple exit rows from the same snapshot all
    go into the same fold.
  - Embargo gap = 48 snapshots (4h) between train and test.
  - pos_roe_drawdown handles negative ROE correctly (no division by near-zero).

Usage:
    python -m satellite.experiments.exp_exit_model --db storage/satellite.db
"""

import logging
import sys

import numpy as np

from satellite.features import FEATURE_NAMES
from satellite.experiments.harness import (
    load_simulated_exits,
    run_walkforward,
    run_permutation_baseline,
    summarize,
    print_report,
    save_report,
    get_standard_args,
    XGBOOST_BINARY,
    SNAPSHOTS_PER_DAY,
    MIN_TRAIN_DAYS,
    EMBARGO_SNAPSHOTS,
    TEST_DAYS,
)

log = logging.getLogger(__name__)

EXPERIMENT_NAME = "exit_model"
DESCRIPTION = "Predict hold vs exit at each 5m checkpoint (binary, 19 features)"

# Position context feature names
POSITION_FEATURES = [
    "pos_current_roe",
    "pos_roe_drawdown",
    "pos_time_in_position",
    "pos_roe_velocity",
    "pos_side_encoded",
]


def build_exit_dataset(rows: list[dict]) -> tuple[list[dict], list[str]]:
    """Build feature matrix from simulated exit rows.

    Each row already has the 14 market features from the JOIN.
    We add 5 position context features computed from the simulated exit fields.

    Returns:
        Tuple of (processed_rows, feature_names).
    """
    feature_names = list(FEATURE_NAMES) + POSITION_FEATURES
    processed = []

    # Group by (snapshot_id, side) to compute running peak ROE
    groups: dict[tuple[str, str], list[dict]] = {}
    for row in rows:
        key = (row["snapshot_id"], row["side"])
        if key not in groups:
            groups[key] = []
        groups[key].append(row)

    for key, group in groups.items():
        # Sort by checkpoint_time within group
        group.sort(key=lambda r: r["checkpoint_time"])
        peak_roe = -999.0
        prev_roe = None

        for row in group:
            # Skip rows with missing market features
            if any(row.get(f) is None for f in FEATURE_NAMES):
                continue

            current_roe = row["current_roe"]

            # Track peak ROE
            if current_roe > peak_roe:
                peak_roe = current_roe

            # Position context features
            row["pos_current_roe"] = current_roe

            # ROE drawdown from peak: how much has it faded?
            # 0.0 = at peak, negative = below peak
            # This avoids division-by-near-zero issues with ratio approach
            row["pos_roe_drawdown"] = current_roe - peak_roe

            # Time in position (minutes)
            snap_time = row.get("snap_created_at", 0)
            row["pos_time_in_position"] = (
                (row["checkpoint_time"] - snap_time) / 60.0
            )

            # ROE velocity: change from previous checkpoint
            if prev_roe is not None:
                row["pos_roe_velocity"] = current_roe - prev_roe
            else:
                row["pos_roe_velocity"] = 0.0

            # Side encoding
            row["pos_side_encoded"] = 1.0 if row["side"] == "long" else -1.0

            prev_roe = current_roe
            processed.append(row)

    return processed, feature_names


def main():
    parser = get_standard_args()
    args = parser.parse_args()

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s [%(name)s] %(levelname)s: %(message)s",
        datefmt="%H:%M:%S",
    )

    log.info("Loading simulated exits for %s...", args.coin)
    raw_rows = load_simulated_exits(args.db, args.coin)
    log.info("Loaded %d simulated exit rows", len(raw_rows))

    if not raw_rows:
        log.error("No simulated exit data found. Run labeler first.")
        sys.exit(1)

    log.info("Building exit model dataset...")
    valid_rows, feature_names = build_exit_dataset(raw_rows)
    log.info("Valid rows after processing: %d", len(valid_rows))

    if not valid_rows:
        log.error("No valid rows after processing")
        sys.exit(1)

    # Sort by snap_created_at to maintain temporal order for walk-forward
    valid_rows.sort(key=lambda r: (r.get("snap_created_at", 0), r.get("checkpoint_time", 0)))

    X = np.array([[row[f] for f in feature_names] for row in valid_rows], dtype=np.float32)
    y = np.array([row["should_hold"] for row in valid_rows], dtype=np.float32)

    log.info("Feature count: %d (14 market + 5 position)", len(feature_names))
    log.info("Class balance: %.1f%% hold, %.1f%% exit", y.mean() * 100, (1 - y.mean()) * 100)

    # Adjust walk-forward step for exit model:
    # Each snapshot produces ~12 exit rows (6 checkpoints x 2 sides).
    # So N exit rows = N/12 snapshots. The embargo must be in snapshot time,
    # not row count. We scale the embargo accordingly.
    rows_per_snapshot = len(valid_rows) / max(1, len(set(r["snapshot_id"] for r in valid_rows)))
    scaled_embargo = int(EMBARGO_SNAPSHOTS * rows_per_snapshot)
    log.info("Rows per snapshot: %.1f, scaled embargo: %d rows", rows_per_snapshot, scaled_embargo)

    # Permutation baseline
    baseline = 0.0
    if not args.no_baseline:
        baseline = run_permutation_baseline(
            X, y, feature_names, XGBOOST_BINARY, EXPERIMENT_NAME, is_binary=True,
        )

    results, importance = run_walkforward(
        X, y, feature_names, XGBOOST_BINARY, EXPERIMENT_NAME,
        is_binary=True, embargo=scaled_embargo,
    )

    summary = summarize(EXPERIMENT_NAME, DESCRIPTION, feature_names, len(valid_rows), results, importance, baseline)
    print_report(summary)
    save_report(summary)

    # Additional analysis: which position features matter?
    if importance:
        pos_imp = {f: importance.get(f, 0) for f in POSITION_FEATURES}
        market_imp = {f: importance.get(f, 0) for f in FEATURE_NAMES}
        total_pos = sum(pos_imp.values())
        total_market = sum(market_imp.values())
        total = total_pos + total_market
        if total > 0:
            print(f"\n  Position features contribute: {total_pos / total * 100:.1f}% of total gain")
            print(f"  Market features contribute: {total_market / total * 100:.1f}% of total gain")
            print("\n  Position feature breakdown:")
            for f, v in sorted(pos_imp.items(), key=lambda x: -x[1]):
                print(f"    {f:<30} gain={v:.2f}")


if __name__ == "__main__":
    main()
